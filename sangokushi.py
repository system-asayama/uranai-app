def get_character(index: int) -> Character:
    """干支index(0..59)に対応する三国志キャラクターを返す。"""
    name, yomi, faction, title, reason = _DATA[index]
    return Character(index, name, yomi, faction, title, reason,
                     history=_HISTORY[index])


def all_characters() -> list[Character]:
    return [get_character(i) for i in range(60)]


