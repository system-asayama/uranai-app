def julian_day_number(d: date) -> int:
    """グレゴリオ暦の日付からユリウス通日(JDN)を求める。"""
    a = (14 - d.month) // 12
    y = d.year + 4800 - a
    m = d.month + 12 * a - 3
    return (
        d.day
        + (153 * m + 2) // 5
        + 365 * y
        + y // 4
        - y // 100
        + y // 400
        - 32045
    )


def day_pillar_index(d: date, after_23: bool = False) -> int:
    """日柱の干支インデックス(0=甲子 .. 59=癸亥)を返す。

    四柱推命では日の変わり目が23時（子の刻）のため、
    23時以降生まれの場合は翌日の干支として扱う。
    """
    jdn = julian_day_number(d)
    if after_23:
        jdn += 1
    return (jdn + 49) % 60


def ganzhi_name(index: int) -> str:
    return STEMS[index % 10] + BRANCHES[index % 12]


def ganzhi_yomi(index: int) -> str:
    return STEM_YOMI[index % 10] + BRANCH_YOMI[index % 12]


