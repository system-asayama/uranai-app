def get_fortune(index: int) -> Fortune:
    """干支インデックス(0..59)から鑑定結果を返す。"""
    t = _TEXTS[index]
    return Fortune(
        index=index,
        name=ganzhi_name(index),
        yomi=ganzhi_yomi(index),
        element=STEM_ELEMENT[index % 10],
        yinyang=STEM_YINYANG[index % 10],
        animal=BRANCH_ANIMAL[index % 12],
        catch=t["catch"],
        seikaku=t["seikaku"],
        shigoto=t["shigoto"],
        renai=t["renai"],
        lucky=t["lucky"],
    )


def divine(birth: date, after_23: bool = False) -> Fortune:
    """生年月日（と23時以降フラグ）から日柱の鑑定結果を返す。"""
    return get_fortune(day_pillar_index(birth, after_23))


def all_fortunes() -> list[Fortune]:
    """六十干支すべての鑑定結果を一覧で返す。"""
    return [get_fortune(i) for i in range(60)]
